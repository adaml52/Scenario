#!/bin/bash

#Install Puppet repositories for RH7 and update packages

yum install epel-release -y && yum update -y

#Install Puppet software and start the service

yum install puppet -y && systemctl restart puppet

#Configure puppet.conf

sudo cat >> EOF

[main]
    # The Puppet log directory.
    # The default value is '$vardir/log'.
    logdir = /var/log/puppet

    # Where Puppet PID files are kept.
    # The default value is '$vardir/run'.
    rundir = /var/run/puppet

    # Where SSL certificates are kept.
    # The default value is '$confdir/ssl'.
    ssldir = $vardir/ssl

    server = puppetmaster

[agent]
    # The file in which puppetd stores a list of the classes
    # associated with the retrieved configuratiion.  Can be loaded in
    # the separate ``puppet`` executable using the ``--loadclasses``
    # option.
    # The default value is '$confdir/classes.txt'.
    classfile = $vardir/classes.txt

    # Where puppetd caches the local configuration.  An
    # extension indicating the cache format is added automatically.
    # The default value is '$confdir/localconfig'.
    localconfig = $vardir/localconfig

EOF

#Generate client certificate and request signage from puppetmaster

sudo puppet agent -t --waitforcert 60
